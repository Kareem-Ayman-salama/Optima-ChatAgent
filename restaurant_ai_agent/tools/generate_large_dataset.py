def slug(text: str) -> str:
    mapping = {
        "ش": "sh",
        "ا": "a",
        "أ": "a",
        "إ": "a",
        "آ": "a",
        "و": "w",
        "ي": "y",
        "ى": "y",
        "ر": "r",
        "م": "m",
        "ح": "h",
        "خ": "kh",
        "ف": "f",
        "ك": "k",
        "ة": "h",
        "ت": "t",
        "ن": "n",
        "ص": "s",
        "س": "s",
        "ع": "a",
        "د": "d",
        "ل": "l",
        "ب": "b",
        "ق": "q",
        "ط": "t",
        "ج": "g",
        "ز": "z",
        "غ": "gh",
        "ث": "th",
        "ذ": "z",
        "ض": "d",
        "ظ": "z",
        "ء": "",
        "ئ": "e",
        "ؤ": "o",
        " ": "_",
        "-": "_",
    }
    return "".join(mapping.get(ch, ch) for ch in text).lower().replace("__", "_").strip("_")


def item(
    name: str,
    price: int,
    description: str,
    tags: list[str],
    allergens: list[str] | None = None,
    spice_level: int = 0,
    prep_minutes: int = 18,
    available: bool = True,
    modifiers: list[str] | None = None,
) -> dict:
    item_id = slug(name)
    return {
        "id": item_id,
        "sku": f"BM-{item_id[:18].upper()}",
        "name": name,
        "price_egp": price,
        "description": description,
        "tags": tags,
        "allergens": allergens or [],
        "spice_level": spice_level,
        "prep_minutes": prep_minutes,
        "available": available,
        "modifiers": modifiers or ["مستوى الملح", "بدون بصل", "صوص إضافي"],
        "calories_estimate": max(120, int(price * 3.1)),
        "image_prompt": f"صورة طبق مطعم واقعية لـ {name} على ترابيزة خشب بإضاءة دافئة",
    }


