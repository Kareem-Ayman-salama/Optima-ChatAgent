def write_json(path: Path, data: object) -> None:
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def main() -> None:
    DATA_DIR.mkdir(parents=True, exist_ok=True)
    if KB_PATH.exists() and not SMALL_BACKUP_PATH.exists():
        SMALL_BACKUP_PATH.write_text(KB_PATH.read_text(encoding="utf-8"), encoding="utf-8")

    kb = build_kb()
    write_json(KB_PATH, kb)

    examples = build_intent_examples(kb)
    INTENT_DATASET_PATH.write_text(
        "\n".join(json.dumps(example, ensure_ascii=False) for example in examples) + "\n",
        encoding="utf-8",
    )

    scenarios = build_conversation_scenarios()
    write_json(SCENARIOS_PATH, scenarios)
    write_json(SAMPLE_SCENARIOS_PATH, scenarios)

    item_count = sum(len(category_data["items"]) for category_data in kb["categories"])
    print(f"Generated {KB_PATH}")
    print(f"Categories: {len(kb['categories'])}")
    print(f"Menu items: {item_count}")
    print(f"Intent examples: {len(examples)}")
    print(f"Conversation scenarios: {len(scenarios)}")


if __name__ == "__main__":
    main()
